/*
================================
Functional Requirements
================================

1. User must be authenticated to access the dashboard.

2. Dashboard should display key metrics
   using cards, charts, and tables.

3. Frontend should fetch dashboard data
   from backend APIs.

4. User should be able to filter data
   (e.g. date range, category).

5. Clicking on a widget should allow
   drill-down to detailed views.

6. Proper loading, empty, and error
   states should be shown.
*/

/*
================================
Non-Functional Requirements
================================

1. Performance
   - Fast initial load of dashboard
   - Lazy loading for heavy widgets

2. Scalability
   - Easy to add/remove dashboard widgets
   - Modular, component-based design

3. Security
   - Dashboard routes should be protected
   - Secure API communication

4. Maintainability
   - Clear separation of UI, state, and data logic
   - Reusable components
*/
