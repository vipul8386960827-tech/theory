/**
 * Functional Requirements: describe WHAT the system should do (features, user actions)
 * Examples:
 *   - User can sign up, log in, and log out
 *   - User can view a list of items
 *   - User can search, filter, and paginate
 *   - User can create, edit, or delete data
 *   - User can upload files
 *   - Admin can manage users
 *   - System sends notifications
 *
 * Non-Functional Requirements: describe HOW well the system should work (quality, constraints)
 * Examples:
 *   - Performance: page should load within 2 seconds, smooth scrolling for large lists
 *   - Scalability: support 10k / 1M users, handle traffic spikes
 *   - Security: secure authentication, prevent XSS/CSRF, protect sensitive data
 *   - Reliability: graceful error handling, retry failed requests, no crashes
 *   - Accessibility: keyboard navigation, screen reader support, WCAG compliance
 *   - Usability: responsive design, clear UX, works across devices/browsers
 *   - SEO: server rendering for crawlability, metadata support
 *
 * One-Line Difference:
 *   - Functional = what the system does
 *   - Non-Functional = how well the system does it
 *
 * Example: Login Feature
 *   - Functional:
 *       - User can enter email & password
 *       - User can submit the form
 *       - User is redirected after login
 *   - Non-Functional:
 *       - Login should be secure
 *       - Page should load fast
 *       - Tokens should not be exposed
 *       - Works on mobile
 */
