/* 
2. Select by ID in CSS:

Definition:
- The ID selector targets a **single, unique HTML element** with a specific id attribute.
- Syntax: #idName { property: value; }
- IDs should be **unique within a page**.

Example:

<head>
  <style>
    // ID selector
    #main-title {
      color: blue;
      text-align: center;
      font-size: 24px;
    }
  </style>
</head>
<body>
  <h1 id="main-title">Welcome to My Website</h1>
</body>

// Key Points:
// - ID selector specificity: 100 (higher than class and element selectors).
// - Only one element should have a given ID on a page.
// - Useful for unique sections or elements like headers, main titles, or forms.
*/
