/* 
2. Purpose of CSS (Cascading Style Sheets):

1. **Separation of Content and Presentation**:
   - CSS allows you to keep HTML for structure/content and CSS for styling.
   - Makes code cleaner, easier to maintain, and scalable.

2. **Consistent Design Across Pages**:
   - One CSS file can style multiple pages, ensuring uniform look and feel.

3. **Layout and Positioning**:
   - CSS controls the placement of elements on the page using Flexbox, Grid, floats, and positioning.

4. **Styling and Visual Enhancements**:
   - Customize colors, fonts, spacing, borders, backgrounds, shadows, animations, and transitions.

5. **Responsive Design**:
   - CSS enables pages to adapt to different screen sizes using media queries and flexible units.

6. **Accessibility and Readability**:
   - CSS improves readability (contrast, font-size) and accessibility (focus states, visual cues).

7. **Performance Optimization**:
   - External CSS files can be cached by browsers, reducing redundancy and improving load times.

Summary:
- CSS is essential for making web pages **visually appealing, structured, and user-friendly** while keeping HTML clean and semantic.
*/
