/* 
7. Common Display Values in CSS

1. display: block
   // Starts on a new line
   // Takes full width
   // Width/height can be applied

2. display: inline
   // Does NOT start on a new line
   // Only takes required width
   // Width/height cannot be applied

3. display: inline-block
   // Inline behavior + can set width/height

4. display: none
   // Removes the element completely from layout

5. display: flex
   // Converts element into a flex container

6. display: inline-flex
   // Flex container but acts inline

7. display: grid
   // Converts element into a grid container

8. display: inline-grid
   // Grid container but inline

9. display: table
   // Makes element behave like a <table>

10. display: list-item
   // Makes element behave like <li> (with a bullet)

11. display: contents
   // Ignores the element box itself
   // Children behave as if they belong to parent directly

12. display: run-in (rarely used)
   // Element behaves inline or block depending on context

Key Points:
// - block → full width, new line
// - inline → no width/height allowed
// - inline-block → inline + width/height allowed
// - none → hide completely
// - flex/grid → layout systems
// - contents → keeps children but removes parent box
*/
